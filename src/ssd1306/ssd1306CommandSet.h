#ifndef ATMEGA328P_WATERFLOWSVERDRUP_SRC_SSD1306_SSD1306COMMANDSET_H_
#define ATMEGA328P_WATERFLOWSVERDRUP_SRC_SSD1306_SSD1306COMMANDSET_H_

#define	WRITE_COMMAND						0x00
#define	WRITE_DATA							0x01

#define CMD_SET_MUX_RATIO					0xa8
#define MUX_RATIO_RESET						0x63

#define CMD_SET_DISPLAY_OFFSET				0xd3
#define DISPLAY_OFFSET_RESET				0x00

#define CMD_SET_DISPLAY_START_LINE			0b01000000

#define SEGMENT_MAP_NORMAL					0b10100000	//	maps column address 0 to SEG0	(reset)
#define SEGMENT_MAP_REMAPPED				0b10100001	//	maps column address 127 to SEG0

#define COM_OUTPUT_SCAN_NORMAL				0b11000000	//	reset
#define COM_OUTPUT_SCAN_REMAPPED			0b11001000

#define	CMD_SET_COM_PINS_HWCONF				0b11011010
#define COM_PINS_HWCONF						0b00000010	//	reset
#define COM_PINS_HWCONF_SEQUENTIAL			4			//	0 for reset
#define COM_PINS_HWCONF_ALTERNATIVE			4			//
#define COM_PINS_HWCONF_DISABLE_LR_REMAP	5			//	0 for reset
#define COM_PINS_HWCONF_ENABLE_LR_REMAP		5			//

#define CMD_SET_CONTRAST_CONTROL			0b10000001
#define	CONTRAST_RESET						0b01111111

#define	DISPLAY_RAM_CONTENT					0b10100100	//	reset
#define	IGNORE_RAM_CONTENT					0b10100101

#define	SET_NORMAL_DISPLAY					0b10100110
#define SET_INVERSE_DISPLAY					0b10100111

#define SET_CLOCK_DIVIDE_RATIO_OSC_FREQ		0b11010101
#define CLOCK_DIVIDE_RATIO_OSC_FREQ_RESET	0b10000000	//	reset

#define SET_CHARGE_PUMPE					0b10001101
#define	CHARGE_PUMPE_ENABLE					0x14
#define	CHARGE_PUMPE_DISPLAY_ON				0xaf

#define SET_DISPLAY_ON						0b10101111
#define SET_DISPLAY_OFF						0b10101110	//	reset

#define	SET_PAGE_ADDRESS					0x22
#define START_PAGE_RESET					0b0000000
#define END_PAGE_RESET						0b0000111

#define	SET_COLUMN_ADDRESS					0x21
#define START_COLUMN_RESET					0b00000000
#define END_COLUMN_RESET					0b01111111

#endif
